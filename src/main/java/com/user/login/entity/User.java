package com.user.login.entity;

public class User {

	private String username;
	private char[] password;
	private String name;
	private String email;
	
	public String getUsername() {
		return username;
	}
	public char[] getPassword() {
		return password;
	}
	public String getName() {
		return name;
	}
	public String getEmail() {
		return email;
	}
	
	
}
